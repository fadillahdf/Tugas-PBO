# Menghitung luas permukaan dan volume bola
# dibuat oleh fadillah dian firdaus
# Jari-jari bola
jari_jari = 7

# Mengimport nilai pi
from math import pi

# Menghitung luas permukaan bola
luas_permukaan = 4 * pi * (jari_jari ** 2)

# Menghitung volume bola
volume = (4/3) * pi * (jari_jari ** 3)

# Menampilkan hasil
print("Luas Permukaan Bola: ", luas_permukaan)
print("Volume Bola: ", volume)
